// celsius to fahrenheit

let celsius = 14;

let celsiusToFahrenheit = celsius * 1.8(9/5) + 32

console.log(`convert celsius ${celsius} to fahrenheit is : ${celsiusToFahrenheit}`);


// fahrenheit to celsius

let fahrenheit = 57.2

let fahrenheitToCelsius = (fahrenheit - 32) * 5/9

console.log(`convert fahrenheit ${fahrenheit} to celsius is : ${fahrenheitToCelsius}`);
